numbers = [4,2,9,1,5,6]

length = len(numbers)
total_sum = sum(numbers)
max_value = max(numbers)
min_value = min(numbers)
sorted_numbers = sorted(numbers)
bool_list = [False, True, False]
any_true = any(bool_list)
all_true = all(bool_list)
string = "hello"
char_list = list(string)
reversed_numbers = list(reversed(numbers))
enumerated_numbers = list(enumerate(numbers))
print(f'{length}\n, {total_sum}\n, {max_value}\n, {min_value}\n, {sorted_numbers}\n, {any_true}\n, {all_true}\n, {char_list}\n, {reversed_numbers}\n, {enumerated_numbers}')